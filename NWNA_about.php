    <p>The North Westdale Neighborhood Association, a nonprofit, nonpartisan community advocacy organization, represents approximately 1000 homes in the North Westdale neighborhood of Los Angeles. The NWNA serves the area bounded by the 405 Freeway to the east, National Boulevard to the south, Centinela Avenue/Bundy Drive to the west and the Santa Monica Freeway to the north. All residents, both homeowners and renters, are members. There is an annual voluntary donation of $10 per household.</p>
    <p>Every block has a "block captain", a representative who attends NWNA monthly meetings and distributes the newsletter. You are encouraged to keep in touch with your block captain or attend the meetings yourself. The meetings in September through May focus on concerns within our immediate neighborhood. The June meeting offers a public opportunity for all residents to meet our city council representative, police captain, and other community leaders.</p>
    <p>The NWNA coincides with Zone 2 of the Mar Vista Neighborhood Council, part of the citywide system of neighborhood councils established by the 1999 City Charter.</p>

    <h2>Safety</h2>
    <p>The NWNA employs Security Service Systems to patrol our neighborhood. The service costs each household about $30 a month (voluntary fee.) For each 28 paying households, we receive an additional hour of coverage per day. Paying households qualify for additional services.</p>
    <p>The NWNA originated in the 1980s as a crime watch. The Los Angeles Police Department has observed that crime in our area is significantly lower as a result of our organization.</p>

    <h2>Community Improvement</h2>
    <p>The NWNA monitors city services such as tree-trimming, paving, sanitation, recycling, utilities, and traffic control, and works together with the relevant departments to maintain a better neighborhood infrastructure.</p>
    <p>The NWNA stays in contact with Caltrans for improved maintenance of the planted area along the freeway, and Project Heavy West for removal of graffiti.</p>
    <p>The NWNA sends volunteers to Emergency Response Team training with the Los Angeles Fire Department, to work as a neighborhood rescue team in the event of a major earthquake or other emergency.</p>
    <p>We initiate beautification projects for the neighborhood. We participated in the establishment of the school garden at the corner of Brookhaven and Colby Avenues, and planted street trees along Pico Boulevard and Brookhaven Avenue.</p>
    <p>The NWNA participated in a successful campaign to halt development of a large business park at the east side of the Santa Monica airport that would have increased traffic and pollution in our neighborhood.</p>

    <h2>Community Awareness</h2>
    <p>We maintain close contact with our city council representative, our local police officers, and Security Service Systems. They want to hear our concerns.</p>
